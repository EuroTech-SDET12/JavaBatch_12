package com.days.day10;

public class HiFive {

    public static void main(String[] args) {

        int number = -30;


        //If the number is a multiple of 5
        if (number % 5 == 0){
            System.out.println("Hi Five!" );
        }

        if(number % 2 == 0){
            System.out.println("Hi Even!");
        }

    }
}
