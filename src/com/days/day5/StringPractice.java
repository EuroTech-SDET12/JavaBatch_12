package com.days.day5;

public class StringPractice {

    public static void main(String[] args) {

        // String is a sequence of characters
        String word = "repowruw3prewpoe4u2904iejfwerw09f23 3295 oqw we wqe£$!$£%$^&%^&**(())_~@@L:P:$243";
        System.out.println(word);

        word = "1";
        System.out.println(word);

        int num = 1;
        System.out.println(num);

        char charValue = '1';
        System.out.println(charValue);

        // word, number and charValue variables looks have same values.
        // But their data types are DIFFERENT!!!


    }
}
