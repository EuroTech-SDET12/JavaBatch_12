package com.days.day7;

public class EscapeSequences {
    public static void main(String[] args) {
        //write some note
        /*
        you can write some
        notes
        between  and
         */
        System.out.println("hello!!!");
        /*
        will be passed
        will not be executed
         */
        System.out.println("Hi!!!");

        System.out.println(" Ali came and said: \"Helloo !!\"      ");
//                 Ali came and said: "Helloo" !!"
        System.out.println(" \\\\ ");
        System.out.println("   ");
        System.out.println("     /\\");
        System.out.println("    /  \\");
        System.out.println("   /    \\");
        System.out.println("  /      \\");
        System.out.println(" /        \\");
        System.out.println("/__________\\");
        System.out.println("Variables Types :");
        System.out.println("byte, short, int ,long ....");
        System.out.println("Variables Types :\nbyte,\nshort,\nint ,\nlong ....");
        System.out.println("Variables Types :\"byte\",'short',\tint , \blong ....");
    }
}
